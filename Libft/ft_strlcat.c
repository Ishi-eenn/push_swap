#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	dst_len;
	size_t	src_len;

	src_len = ft_strlen(src);
	if (dstsize == 0)
		return (src_len);
	dst_len = ft_strlen(dst);
	if (dstsize <= dst_len)
		return (dstsize + src_len);
	ft_strlcpy(&dst[dst_len], src, dstsize - dst_len);
	if (dstsize > dst_len)
		dstsize = dst_len;
	return (dstsize + src_len);
}
