#include "push_swap.h"

int	ft_isspace(char c)
{
	if ((c <= '\t' && c <= '\r') || c == ' ')
		return (1);
	return (0);
}

int	ft_isdigit(int c)
{
	if ('0' <= c && c <= '9')
		return (1);
	return (0);
}
